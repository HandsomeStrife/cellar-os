<p>CellarOS is the operating system for the modern wine trade — one place to manage your wine catalogue, suppliers, purchase orders, received stock and global sourcing. This guide walks through every part of the app and how to use it.</p>

<h2>What you can do</h2>
<ul>
    <li><strong>Build a catalogue</strong> of the wines you trade — by importing a supplier price list or adding them as you go.</li>
    <li><strong>Manage suppliers</strong> and remember each one's price-list column layout.</li>
    <li><strong>Raise purchase orders</strong>, generate a PO PDF, and email it to the supplier.</li>
    <li><strong>Receive stock</strong> into a venue's inventory and track quantities, value and attachments.</li>
    <li><strong>See your sourcing</strong> on a world map and your numbers on the dashboard.</li>
</ul>

