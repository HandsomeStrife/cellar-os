<ol>
    <li><strong>Create an account</strong> — pick your base currency and name your venue. See <a href="{{ url('/guide/accounts') }}" wire:navigate>Accounts, venues &amp; plans</a>.</li>
    <li><strong>Add a supplier</strong> — see <a href="{{ url('/guide/suppliers') }}" wire:navigate>Suppliers</a>.</li>
    <li><strong>Import their price list</strong> (or add wines manually) — see <a href="{{ url('/guide/import') }}" wire:navigate>Importing price lists</a>.</li>
    <li><strong>Build an order</strong> from the catalogue basket — see <a href="{{ url('/guide/catalogue') }}" wire:navigate>Catalogue</a> and <a href="{{ url('/guide/orders') }}" wire:navigate>Purchase orders</a>.</li>
    <li><strong>Send &amp; receive</strong> the order to stock your <a href="{{ url('/guide/inventory') }}" wire:navigate>inventory</a>.</li>
</ol>

